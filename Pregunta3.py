PrimerV=float(input("Ingrese Primer Valor: "))
SegundoV=float(input("Ingrese Segundo Valor: "))
TercerV=float(input("Ingrese Tercer Valor: "))

Suma=PrimerV+SegundoV+TercerV
Resta=PrimerV-SegundoV-TercerV
Multi=PrimerV*SegundoV*TercerV
Div=PrimerV/SegundoV/TercerV
DivEntera=PrimerV//SegundoV//TercerV
print(f"La Suma de los valores es: {Suma}")
print(f"La Resta de los valores es: {Resta}")
print(f"La Multiplicación de los valores es: {Multi}")
print(f"La División de los valores es: {Div}")
print(f"La División Entera de los valores es: {DivEntera}")
